public class Patient {
    String patient_id;
    String patient_surname;
    String patient_name;
    String patient_address;
    String patient_phone_number;

    public Patient(String patient_id, String patient_surname, String patient_name, String patient_address, String patient_phone_number) {
        this.patient_id = patient_id;
        this.patient_surname = patient_surname;
        this.patient_name = patient_name;
        this.patient_address = patient_address;
        this.patient_phone_number = patient_phone_number;
    }
}
